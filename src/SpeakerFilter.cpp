/*

Matlab script to generate impulses.
Requires noise.wav, 44.1 kHz recording of a SOUND 1,0,300,15,0,0,1 command
(AY3 noise of period 1, 3 s duration).
  
clear;

[x, fs] = wavread ('noise.wav');
x = transpose (x);

fft_len = 128;

% Scans noise to acquire spectrum
y = linspace (0, 0, fft_len);
nbr_scan = 0;
win = blackman (fft_len + 2);
win = transpose (win (2:fft_len+1));	% Removes terminal zeroes
for k = 0 : fft_len/16 : (length (x) - fft_len)
   xx = x (k+1:k+fft_len);
   xx = xx .* win;
   yy = fft (xx);
   yy_abs = abs (yy);
   y = y + yy_abs;
   nbr_scan = nbr_scan + 1;
end
p = sqrt (sum (y .^ 2) / fft_len);
y = y / p;

% Generates minimum-phase impulse
o = 16;	% Oversampling for cepstrum calculation
xx = real (ifft (y));
len = length (xx);
xx = [xx(len/2+1:len) xx(1:len/2)];
[yn, ym] = rceps ([xx zeros(1, len*(o-1))]);
x1 = ym(1:len);	% Final result

*/

