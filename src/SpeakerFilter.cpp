SpeakerFilter::SpeakerFilter ()
:	_convolver ()
{
	// Nothing
}



void	SpeakerFilter::set_quality (int quality)
{
	assert (quality >= 0);
	assert (quality < 3);

	const float		vol = 0.5f;
	switch (quality)
	{
	case	0:
		_convolver.set_impulse (128, _impulse_128, vol);
		break;
	case	1:
		_convolver.set_impulse (512, _impulse_512, vol);
		break;
	case	2:
		_convolver.set_impulse (2048, _impulse_2048, vol);
		break;
	default:
		assert (false);
		break;
	}
}



long	SpeakerFilter::get_block_len () const
{
	return (_convolver.get_block_len ());
}



float *	SpeakerFilter::get_input_buffer () const
{
	return (_convolver.get_input_buffer ());
}



const float *	SpeakerFilter::get_output_buffer () const
{
	return (_convolver.get_output_buffer ());
}



void	SpeakerFilter::process (long nbr_spl)
{
	assert (nbr_spl > 0);
	assert (nbr_spl <= get_block_len ());

	const long		rem_len = get_block_len () - nbr_spl;
	if (rem_len > 0)
	{
		using namespace std;

		float *			in_buf_ptr = get_input_buffer ();
		memset (&in_buf_ptr [nbr_spl], 0, sizeof (*in_buf_ptr) * rem_len);
	}

	_convolver.process ();
}



void	SpeakerFilter::clear_buffers ()
{
	_convolver.clear_buffers ();
}



